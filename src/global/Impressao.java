package global;

public class Impressao {

    public static void imprimirVetor(int[] vetor) {
        for (int j = 0; j < vetor.length; j++) {
            System.out.print(vetor[j] + " ");
        }
    }

    public static void imprimirVetorString(String[] vetor) {
        for (int j = 0; j < vetor.length; j++) {
            System.out.print(vetor[j] + " ");
        }
    }

}
